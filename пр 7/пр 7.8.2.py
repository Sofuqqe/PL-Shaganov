def swap_first_last(arr):
    """Процедура для замены первого и последнего элементов массива."""
    if len(arr) > 1:  # Проверяем, что массив содержит больше одного элемента
        arr[0], arr[-1] = arr[-1], arr[0]

# Ввод длины массива
m = int(input("Введите длину массива: "))

# Ввод элементов массива
A = []
print(f"Введите {m} элементов массива:")
for i in range(m):
    element = int(input(f"Элемент {i + 1}: "))
    A.append(element)

# Вывод исходного массива
print("Исходный массив:", A)

# Замена первого и последнего элементов
swap_first_last(A)

# Вывод изменённого массива
print("Полученный массив:", A)
